#include <stdio.h>

int main (int argc, char *argv[]) {  // Указываем сигнатуру с аргументами командной строки
    int i = 2;                      // Начальный индекс
    int arge = 4;                   // Конечный индекс

    // Проверяем, что аргументов достаточно
    if (argc <= arge) {
        printf("Недостаточно аргументов. Требуется как минимум %d аргументов.\n", arge);
        return 1;  // Завершаем программу с кодом ошибки
    }

    char **pattern  = &argv[i];
    char **end = &argv[arge]; 

    for (char **p = pattern; p < end; ++p) {
        printf("%s\n", *p);  // Выводим каждый аргумент в указанном диапазоне
    }

    return 0;  // Успешное завершение программы
}
